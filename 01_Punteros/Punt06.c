//---------------------------------------------------------------------------
#include<stdio.h>
//---------------------------------------------------------------------------
int main(void)
{
    int *pi,i;
    char *pc;
    float *pf;
    pi=pc=pf=0x0000;
    for(i=0;i<10;i++)
    {
        printf("%p\t%p\t%p\n",pi,pf,pc);
        pi++;
        pf++;
        pc++;
    }
    printf("%p\t%p\t%p\n\n",pi,pf,pc);
    return 0;
}
