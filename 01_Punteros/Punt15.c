//---------------------------------------------------------------------------
#include<stdio.h>
//---------------------------------------------------------------------------
#define n 5
//---------------------------------------------------------------------------
int main(void)
{
    int vec[n],*p,*max,i;
    for(p=vec;p<vec+n;p++)
        scanf("%d",p);
    for(max=p=vec,i=0;i<n;i++)
        if(*(p+i)>*max)
            max=p+i;
    printf("\n\nmax=%d",*max);
    printf("\npos=%d\n",max-vec);
    return 0;
}
