int fibo(int);
//---------------------------------------------------------------------------
int main(void)
{
    int x,f;
    scanf("%d",&x);
    f=fibo(x);
    printf("\n%d\t%d",x,f);
    return 0;
}
//---------------------------------------------------------------------------
int fibo(int n)
{
    int a;
    if(n<=2)
        a=1;
    else
        a=fibo(n-2)+fibo(n-1);
    return a;
}
