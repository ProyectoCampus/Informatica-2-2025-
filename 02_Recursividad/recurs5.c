int fibo(int n);
int Fibo(int n);
//---------------------------------------------------------------------------
int main(void)
{
    int n,fi,fr;
    scanf("%d",&n);
    fi=fibo(n);
    fr=Fibo(n);
    printf("\n\n%d\t\t%d\n",fi,fr);
    return 0;
}
//---------------------------------------------------------------------------
int fibo(int n)
{
    int fn1,fn2,f,i;
    fn1=1;
    fn2=1;
    if((n==1)||(n==0))
        return 1;
    for(i=2;i<=n;i++)
    {
        f=fn1+fn2;
        fn2=fn1;
        fn1=f;
    }
    return f;
}
//---------------------------------------------------------------------------
int Fibo(int n)
{
    if(n>1)
        return Fibo(n-1)+Fibo(n-2);
    if((n==1)||(n==0))
        return 1;
}
