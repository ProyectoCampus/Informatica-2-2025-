#include <stdio.h>
//---------------------------------------------------------------------------
struct A
{
	int i;
	char c[10];
};
//---------------------------------------------------------------------------
int main(void)
{
	struct A e1,*pe1;
	int i;
	e1.i=22;
	for(i=0;i<10;i++)
		e1.c[i]='A'+i;
	pe1=&e1;
	(*pe1).i=29;
	pe1->c[4]='#';
    return 0;
}
