//---------------------------------------------------------------------------
#include <stdio.h>
//---------------------------------------------------------------------------
struct A
{
	int a;
	char b;
	float c;
};
//---------------------------------------------------------------------------
struct AR
{
	int i;
	struct AR *p;
};
//---------------------------------------------------------------------------
int main(void)
{
	struct A var,*ps;
	struct AR PEPE,x;
	var.b='C';
	ps=&var;
	(*ps).c=3.14;
	ps->a=7;
	PEPE.i=6;
	x.i=3;
	PEPE.p=&x;
	x.p=&x;
    return 0;
}
