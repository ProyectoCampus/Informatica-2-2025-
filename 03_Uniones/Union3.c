union alfa
{
	int x;
	char y[10];
	float z;
};
//---------------------------------------------------------------------------
int suma(int,int);
int ingreso(void);
//---------------------------------------------------------------------------
int main(void)
{
	union alfa A,B;
	clrscr();
	A.x=ingreso();
	B.x=ingreso();
	printf("\n%d+%d=",A.x,B.x);
	A.x=suma(A.x,B.x);
	printf("%d\n",A.x);
	fflush(stdin);
	gets(A.y);
	if(A.y[2]>'F')
	{
		A.z=3.14;
		scanf("%f",&B.z);
		printf("\n\nPerimetro = %f",A.z*B.z);
	}
    return 0;
}
//---------------------------------------------------------------------------
int ingreso(void)
{
	int a;
	scanf("%d",&a);
	return(a);
}
//---------------------------------------------------------------------------
int suma(int a,int b)
{
	int c;
	c=a+b;
	return(c);
}
