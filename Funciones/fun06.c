#include<stdio.h>
//---------------------------------------------------------------------------
void ingre(int *,int *);
//---------------------------------------------------------------------------
int main(void)
{
    int i,vec[10],*pi;
    pi=vec;
    while(pi<vec+10)
    {
        ingre(pi,vec);
        for(i=0;i<10;i++)
            printf("\n %d",vec[i]);
    }
    return 0;
}
//---------------------------------------------------------------------------
void ingre(int *pc,int *pv)
{
    if(pc<pv+10)
    {
        scanf("%d",pc);
        pc++;
    }
}
