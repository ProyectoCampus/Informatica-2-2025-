int fibo(int);
//---------------------------------------------------------------------------
int main(void)
{
    int x,f;
    scanf("%d",&x);
    f=fibo(x);
    printf("\n%d\t%d\n",x,f);
    return 0;
}
//---------------------------------------------------------------------------
int fibo(int n)
{
    int aux2,aux1,r,i;
    aux2=aux1=r=1;
    for(i=2;i<n;i++)
    {
        aux2=aux1;
        aux1=r;
        r=aux2+aux1;
    }
    return r;
}
