int x,y;
//---------------------------------------------------------------------------
int Suma(int,int);
void SumaGlobal(void);
//---------------------------------------------------------------------------
int main(void)
{
    int i,j;
    i=Suma(4,7);
    x=3;
    y=9;
    SumaGlobal();
    j=Suma(i,6);
    return 0;
}
//---------------------------------------------------------------------------
void SumaGlobal(void)
{
    y+=x;
    y++;
    x++;
}
//---------------------------------------------------------------------------
int Suma(int j,int i)
{
    int k;
    k=j+i;
    j--;
    i++;
    return k;
}
